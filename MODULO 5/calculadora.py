alto = float(input("Ingrese el valor del alto del rectángulo: "))
ancho = float(input("Ingrese el valor del ancho del rectángulo: "))
    
area = alto * ancho
    
perimetro = 2 * (alto + ancho)
    

superficie = 2 * (alto * ancho)
    

print(f"El área del rectángulo es: {area}")
print(f"El perímetro del rectángulo es: {perimetro}")
print(f"La superficie del rectángulo es: {superficie}")

